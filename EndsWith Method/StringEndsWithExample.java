
// Program to demonstrate the use of .endsWith() method in Java

public class StringEndsWithExample  
{
    public static void main(String [] args)  
    {
        String name="Student";
        if(name.endsWith("ent"))
        {
            System.out.println("Correct.");
        }
        else
        {
            System.out.println("Wrong.");
        }
    }
}
