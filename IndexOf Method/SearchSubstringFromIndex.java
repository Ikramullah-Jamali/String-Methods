
public class SearchSubstringFromIndex 
{
    public static void main(String [] args)
    {
        String name="daniyaldaniyal";
        // Find the index of "al" starting from index 10
        System.out.println("From Index: "+name.indexOf("al",10));

        // Find the index of 'a' starting from index 7
        System.out.println("From Index: "+name.indexOf('a',7));

    }
}
